package jenkim49.accord;

public class Accord {
	private String acd_name;
	private String acd_des;
	private String acd_img;

	public Accord() {
	}

	@Override
	public String toString() {
		return "Accord [acd_name=" + acd_name + ", acd_des=" + acd_des + ", acd_img=" + acd_img + "]";
	}

	public Accord(String acd_name, String acd_des, String acd_img) {
		super();
		this.acd_name = acd_name;
		this.acd_des = acd_des;
		this.acd_img = acd_img;
	}

	public String getAcd_name() {
		return acd_name;
	}

	public void setAcd_name(String acd_name) {
		this.acd_name = acd_name;
	}

	public String getAcd_des() {
		return acd_des;
	}

	public void setAcd_des(String acd_des) {
		this.acd_des = acd_des;
	}

	public String getAcd_img() {
		return acd_img;
	}

	public void setAcd_img(String acd_img) {
		this.acd_img = acd_img;
	}

}

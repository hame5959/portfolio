package jenkim49.board;

import java.util.Date;

public class Board {

	private int b_no;
	private String b_title;
	private String m_id;
	private int b_count;
	private String b_content;
	private Date b_date;
	
	public Board() {
	}
	public Board(int b_no, String b_title, String m_id, int b_count, String b_content, Date b_date) {
		super();
		this.b_no = b_no;
		this.b_title = b_title;
		this.m_id = m_id;
		this.b_count = b_count;
		this.b_content = b_content;
		this.b_date = b_date;
	}
	@Override
	public String toString() {
		return "Board [b_no=" + b_no + ", b_title=" + b_title + ", m_id=" + m_id + ", b_count=" + b_count
				+ ", b_content=" + b_content + ", b_date=" + b_date + "]";
	}
	public int getB_no() {
		return b_no;
	}
	public void setB_no(int b_no) {
		this.b_no = b_no;
	}
	public String getB_title() {
		return b_title;
	}
	public void setB_title(String b_title) {
		this.b_title = b_title;
	}
	public String getM_id() {
		return m_id;
	}
	public void setM_id(String m_id) {
		this.m_id = m_id;
	}
	public int getB_count() {
		return b_count;
	}
	public void setB_count(int b_count) {
		this.b_count = b_count;
	}
	public String getB_content() {
		return b_content;
	}
	public void setB_content(String b_content) {
		this.b_content = b_content;
	}
	public Date getB_date() {
		return b_date;
	}
	public void setB_date(Date b_date) {
		this.b_date = b_date;
	}
	
	
	
	
	
	
	}
